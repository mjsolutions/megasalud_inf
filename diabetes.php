                  <h2>Epidemiología</h2>

                  <p>La Diabetes Mellitus es una enfermedad muy frecuente y es un motivo de consulta médica con gran mortalidad actualmente. La forma más frecuente de Diabetes Mellitus es la tipo 2 representando un 90 a 95% del total de Diabetes y la frecuencia está aumentando muy rápidamente debido a los hábitos de vida que prevalecen actualmente y actualmente el 50% de los paciente diabéticos aun no han sido diagnosticados debido a su carácter silente, y 20% de estos presenta ya desafortunadamente complicaciones. Se estima que dentro de 10 años de duplicará el número de pacientes diabéticos tanto de tipo 1 como de tipo 2. Se estima que sólo en México, hay alrededor de 10 millones de diabéticos, es un problema de salud nacional.</p>
