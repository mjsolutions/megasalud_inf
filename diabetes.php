                  <img class="alignright" src="assets/img/mission.png" alt="">
                  <p>En la Diabetes Mellitus tipo 1 suele iniciar antes de los 30 años. El inicio de los síntomas suele ser brusco con síntomas cardinales los cuales son exceso de hambre, sed excesiva la cual no se satisface a la toma de agua, orinar de manera excesiva, necesidad de orinar muy constantemente por la noche a tal grado que el paciente no puede dormir, así como mucha fatiga y pérdida de peso en más del 10% sin causas aparentes. . Muchos de estos pacientes suelen iniciar con trastornos de complicaciones como una cetoacidosis diabética.</p>
                  
                  <p>La Diabetes Mellitus tipo 2 suele comenzar en edades intermedias o avanzadas de la vida y suele irse instaurando de manera gradual a diferencia de la Diabetes Mellitus tipo 1 y en muchos casos el hallazgo es casual en algún estudio de laboratorio el cual se mando realizar por alguna otra circunstancia. La mayoría de los paciente suelen presentar sobre peso (80% son obesos) pero las manifestaciones clínicas suelen ser similares a las que presenta el paciente con desarrollo de Diabetes Mellitus tipo 1.</p>

                  <p>Actualmente se ha descubierto los factores genéticos específicos de herencia para la Diabetes Mellitus y los defectos mas comunes son los defectos en la acción de la insulina produciendo síndromes insulinoresistentes.</p>

