                <p>Las complicaciones en la Diabetes Mellitus son muy frecuentes y suelen ser secundarias a un mal control de dicha enfermedad siendo las más comunes la cetoacidosis diabética, estado hiperosmolar, pie diabético, retinopatía diabética, las hipoglucemias, impotencia sexual, infartos cerebrales, infartos cardiacos, insuficiencia renal, neuropatías diabéticas, predisposición para el desarrollo de infecciones sobre todo cutáneas, entre otras cosas.</p>
