                     <li class="wow fadeInLeft"><i class="material-icons left txt-blue">&#xE15B;</i>Diabetes Mellitus: Cuando se detecta en el paciente una glucosa en ayuno mayor de 126 mg/dl o mayor de 200 mg/dl en una toma al azar.</li>
