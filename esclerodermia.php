          <p>La esclerodermia es una enfermedad del tejido conjuntivo que involucra cambios en la piel, los vasos sanguíneos, los músculos y los órganos internos. Es un tipo de trastornó autoinmunitario, una afección que ocurre cuando el sistema inmunológico ataca por error y destruye tejido corporal sano.</p>
