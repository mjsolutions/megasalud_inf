          <p>Las enfermedades autoinmunes son trastornos que aparecen cuando el organismo desarrolla una respuesta inmune inadecuada contra sus propios órganos o tejidos. A veces, el sistema inmune deja de reconocer como propios a uno o varios tejidos o componentes del organismo y crea auto anticuerpos, que atacan a las propias células, tejidos y órganos. Todo este proceso genera inflamación y lesión, dando lugar a una enfermedad autoinmune. Se desconoce la causa, pero parece que en muchos casos existe una predisposición genética o hereditaria a desarrollar una enfermedad autoinmune. En algunos tipos de enfermedad autoinmune (como en la fiebre reumática), una bacteria o un virus puede desencadenar la respuesta inmune, y los anticuerpos o las células T atacan a las células normales porque éstas presentan alguna similitud estructural con alguna parte o estructura del germen o microorganismo invasor.</p>

          <p>Los trastornos autoinmunes pueden clasificarse en dos grandes categorías: los que afectan a múltiples órganos (enfermedades autoinmunes sistémicas) y los que afectan a un sólo órgano o tejido (se dice que son localizados). Sin embargo, la diferencia entre un tipo u otro no es siempre fácil de establecer, ya que el efecto de ciertos trastornos autoinmunes localizados se extiende frecuentemente más allá de los tejidos diana, afectando así indirectamente a otros tejidos u órganos.</p>

          <p>En la actualidad hay múltiples tratamientos para los pacientes de enfermedades autoinmunes como cáncer, diabetes, asma, artritis, lupus, etc. Es importante conocer y comparar las opciones antes de seleccionar una. Hay que tomar en cuenta qué tan invasivo es el tratamiento, los efectos secundarios y las probabilidades de que contribuya a mejorar la calidad de vida.</p>

